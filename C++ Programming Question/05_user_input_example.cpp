// Purpose: Demonstrate taking multiple integer inputs from the user

#include <iostream>
using namespace std;

int main() {

    int num1, num2;

    cout << "Please enter two numbers: ";
    cin >> num1 >> num2;

    cout << "The value of num1 is : " << num1 << endl;
    cout << "The value of num2 is : " << num2 << endl;

    return 0;
}
